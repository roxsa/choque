	</div> 

	<div class="panel-body">
     
	<img src="public/img/PLA.jpg" alt="">

	<p>La comida puneña se caracteriza por el calor de un horno de leña, usar sus ollas dev barro y todo el aroma que se relaciona con la madre tierra. La mayoría de sus ingredientes son carnes, tubérculos, hierbas, granos que son utilizados en gran parte en sus platos típicos.
Muchos de sus platillos son originarios del lugar y otros han sido influenciados por sus vecinos bolivianos. Estos ricos potajes los pueden encontrar en los restaurantes, picanterías, ferias, las grandes fiestas patronales y en muchas casas de hospedaje. Se sirven acompañados de chicha morada o de una auténtica chicha de jora y otros con un pisco acholado.
1.  Chupe de quinua
Es un plato muy alimenticio. Es a base de quinua, se cocina en agua fría sin sal. Se le agrega verduras.
2.  Trucha frita
Es un pescado que se adquiere del Lago Titicaca que contiene proteínas, así como vitaminas y minerales.
3.  Caldo de cabeza o Huarjata
Para preparar este plato se debe poner a cocer una cabeza de chancho (cerdo) o de cordero en trozos
4.  Chairo o Chayro
Es una sopa espesa y sabrosa a base de papas, chuño, carne y otros ingredientes. Muy apetitoso y de alto poder nutritivo.
5.  Chicharrón de alpaca
Es un delicioso y nutritivo potaje de Puno. Primero se cuece la carne hasta que se consume el agua, luego con la misma grasa se dora.</p>
	
		
