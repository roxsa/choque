		<h3 class="panel-title">GANADERIA PUNO-PERU</h3>
	</div>
	<div class="panel-body">
	<img src="public/img/ga.jpg" alt="">
	<p>Los inicios de la Empresa Ganadera “El Rosario” se remonta a 1917, cuando el Sr. Jose T. Bellido compra el Fundo Queque Norte y empiezan una larga trayectoria de proyectos de desarrollo en ganadería, agricultura e industria láctea.

Es a partir de los años 50 que el Dr. Jorge Bellido Núñez, gran profesional en Medicina Veterinaria, se hace cargo del Fundo y le da un gran impulso de modernidad en ganadería con mejoramiento genético, manejo de pasturas y forrajes e innovación en la Industria Láctea.

El Dr. Bellido Núñez fue pionero en la inseminación artificial en ovinos y uno de los primeros en lograr la transformación de la industria láctea del departamento de Puno.

Actualmente su hijo el Sr. Jorge Bellido Lopera, que estuvo al lado de su padre desde finales de la década del 70, continúa con el proceso de transformación y modernización del Fundo, logrando tener uno de los Establos líderes en la sierra Peruana y un Hato de Ganado Brown Swiss Registrado de alta producción y adaptadas a más de 4000 m.s.n.m.

Es así que luego de varias décadas se logra cerrar un círculo de producción y productividad que va desde la obtención de leche de buena calidad hasta su transformación en productos lácteos logrados con altos estándares de manufactura, distribuidos en los mercados más exigentes del país.

</p>
		
